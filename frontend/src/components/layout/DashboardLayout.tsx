export default function DashboardLayout({ children }: DashboardLayoutProps) {
  const location = useLocation();
  const navigate = useNavigate();
  const { user, logout } = useAuthStore();
  const role = (user?.role != null ? String(user.role) : '').toUpperCase();
  const isSuperAdmin = role === 'SUPER_ADMIN';

  // Modules from Config DB (localStorage, set at login / loadUser)
  // Builds parent-child hierarchy from API's parent_module_id / parent_module fields
  const visibleNavItems = useMemo(() => {
    const configModules = getAssignedModules();
    const items: { path: string; label: string; parentPath?: string }[] = [
      { path: '/dashboard', label: 'Dashboard' },
    ];

    // Build a map: module id → resolved path (for parent lookup)
    const idToPath: Record<number, string> = {};
    for (const m of configModules) {
      const path = m.path || `/${(m.code || '').toLowerCase().replace(/_/g, '-')}`;
      if (m.id != null) idToPath[m.id] = path;
    }

    for (const m of configModules) {
      const path = m.path || `/${(m.code || '').toLowerCase().replace(/_/g, '-')}`;
      if (!path || path === '/dashboard') continue;

      // Determine parentPath from API's parent_module_id
      let parentPath: string | undefined;
      if (m.parent_module_id != null && idToPath[m.parent_module_id]) {
        parentPath = idToPath[m.parent_module_id];
      }

      items.push({ path, label: m.name || m.code, parentPath });
    }

    // Ensure parent paths exist as top-level items (if parent module wasn't in list)
    const existingPaths = new Set(items.map((i) => i.path));
    for (const m of configModules) {
      if (m.parent_module && m.parent_module.id != null) {
        const parentModPath = idToPath[m.parent_module.id];
        if (parentModPath && !existingPaths.has(parentModPath)) {
          existingPaths.add(parentModPath);
          items.push({
            path: parentModPath,
            label: m.parent_module.name || parentModPath.split('/').pop() || parentModPath,
            parentPath: undefined,
          });
        }
      }
    }

    return items;
  }, [user?.id]);

  // Generic dropdown state: any parent path with children can expand/collapse
  const parentPaths = useMemo(
    () => new Set(visibleNavItems.filter((m) => m.parentPath).map((m) => m.parentPath!).filter(Boolean)),
    [visibleNavItems]
  );
  const getDropdownOpen = (path: string) =>
    path && (location.pathname === path || location.pathname.startsWith(path + '/'));
  const [expandedPaths, setExpandedPaths] = useState<Set<string>>(() => {
    const s = new Set<string>();
    for (const p of parentPaths) {
      if (p && getDropdownOpen(p)) s.add(p);
    }
    return s;
  });
  useEffect(() => {
    setExpandedPaths((prev) => {
      const next = new Set(prev);
      for (const p of parentPaths) {
        if (p && getDropdownOpen(p)) next.add(p);
      }
      return next;
    });
  }, [location.pathname, parentPaths]);

  const topLevelNavItems = useMemo(() => visibleNavItems.filter((m) => !m.parentPath), [visibleNavItems]);

  const handleLogout = async () => {
    await logout();
    navigate('/login');
  };

  // Page access: allow if path is in assigned modules (Config DB) or dashboard/profile
  const isDashboardOrProfile = location.pathname === '/dashboard' || location.pathname === '/profile';
  const assignedPaths = useMemo(
    () => new Set(visibleNavItems.flatMap((m) => [m.path, m.parentPath].filter(Boolean))),
    [visibleNavItems]
  );
  const hasPathAccess = useMemo(() => {
    return (path: string) =>
      assignedPaths.has(path) ||
      Array.from(assignedPaths).some((p) => p && path.startsWith(p + '/'));
  }, [assignedPaths]);
  const allowed = isDashboardOrProfile || hasPathAccess(location.pathname);

  useEffect(() => {
    if (!isDashboardOrProfile && !allowed) {
      navigate('/dashboard', { replace: true });
    }
  }, [isDashboardOrProfile, allowed, navigate]);

  const content = !allowed ? null : children;

  const [sidebarCollapsed, setSidebarCollapsed] = useState(false);

  return (
    <div className="flex h-screen w-full overflow-hidden bg-gray-100">
      <aside className={`${sidebarCollapsed ? 'w-20' : 'w-64'} flex-shrink-0 bg-white text-gray-900 flex flex-col overflow-hidden border-r border-gray-200 shadow-sm transition-all duration-300`}>
        <div className="p-6 flex items-center justify-between">
          {!sidebarCollapsed && <span className="text-2xl font-bold tracking-tight text-black">HRMS</span>}
          <button
            onClick={() => setSidebarCollapsed((prev) => !prev)}
            className={`p-1.5 rounded-lg hover:bg-gray-100 transition-colors ${sidebarCollapsed ? 'mx-auto' : ''}`}
            title={sidebarCollapsed ? 'Expand sidebar' : 'Collapse sidebar'}
          >
            <svg className={`w-5 h-5 text-gray-600 transition-transform duration-300 ${sidebarCollapsed ? 'rotate-180' : ''}`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 19l-7-7 7-7m8 14l-7-7 7-7" />
            </svg>
          </button>
        </div>
        <nav className="flex-1 py-4 px-4 space-y-2 overflow-y-auto">
          {topLevelNavItems.map((mod) => {
            const isActive =
              location.pathname === mod.path || (mod.path && location.pathname.startsWith(mod.path + '/'));
            const icon = ICONS_BY_PATH[mod.path] ?? DEFAULT_ICON;
            const childItems = visibleNavItems.filter((m) => m.parentPath === mod.path);
            const isParentWithChildren = childItems.length > 0;
            const expanded = expandedPaths.has(mod.path);
            const dropdownOpen = mod.path ? getDropdownOpen(mod.path) : false;

            if (isParentWithChildren) {
              return (
                <div key={mod.path} className="space-y-1">
                  <div
                    className={`flex items-center ${sidebarCollapsed ? 'justify-center px-2' : 'gap-3 px-4'} py-3 rounded-xl text-sm font-medium transition-all duration-200 cursor-pointer ${
                      isActive || dropdownOpen
                        ? 'bg-gray-100 text-black shadow-sm'
                        : 'text-gray-900 hover:bg-gray-100 hover:text-black'
                    }`}
                    onClick={() => {
                      if (sidebarCollapsed) {
                        setSidebarCollapsed(false);
                        setExpandedPaths((prev) => new Set(prev).add(mod.path));
                        return;
                      }
                      if (location.pathname === mod.path || (mod.path && location.pathname.startsWith(mod.path + '/'))) {
                        setExpandedPaths((prev) => {
                          const next = new Set(prev);
                          if (next.has(mod.path)) next.delete(mod.path);
                          else next.add(mod.path);
                          return next;
                        });
                      } else {
                        setExpandedPaths((prev) => new Set(prev).add(mod.path));
                        navigate(mod.path);
                      }
                    }}
                    role="button"
                    tabIndex={0}
                    title={sidebarCollapsed ? mod.label : undefined}
                    onKeyDown={(e) => {
                      if (e.key === 'Enter' || e.key === ' ') {
                        e.preventDefault();
                        if (sidebarCollapsed) {
                          setSidebarCollapsed(false);
                          setExpandedPaths((prev) => new Set(prev).add(mod.path));
                          return;
                        }
                        if (location.pathname === mod.path || (mod.path && location.pathname.startsWith(mod.path + '/'))) {
                          setExpandedPaths((prev) => {
                            const next = new Set(prev);
                            if (next.has(mod.path)) next.delete(mod.path);
                            else next.add(mod.path);
                            return next;
                          });
                        } else {
                          setExpandedPaths((prev) => new Set(prev).add(mod.path));
                          navigate(mod.path);
                        }
                      }
                    }}
                  >
                    {icon ?? null}
                    {!sidebarCollapsed && <span className="flex-1">{mod.label}</span>}
                    {!sidebarCollapsed && (
                      <svg
                        className={`w-5 h-5 flex-shrink-0 transition-transform ${expanded ? 'rotate-180' : ''}`}
                        fill="none"
                        viewBox="0 0 24 24"
                        stroke="currentColor"
                      >
                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                      </svg>
                    )}
                  </div>
                  {expanded && !sidebarCollapsed && (
                    <div className="pl-4 space-y-1 border-l-2 border-gray-200 ml-4">
                      {childItems.map((child) => {
                        const childActive = location.pathname === child.path;
                        const childIcon = ICONS_BY_PATH[child.path] ?? DEFAULT_ICON;
                        return (
                          <Link
                            key={child.path}
                            to={child.path}
                            className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all duration-200 ${
                              childActive
                                ? 'bg-gray-100 text-black'
                                : 'text-gray-700 hover:bg-gray-100 hover:text-black'
                            }`}
                          >
                            {childIcon ?? null}
                            <span>{child.label}</span>
                          </Link>
                        );
                      })}
                    </div>
                  )}
                </div>
              );
            }

            return (
              <Link
                key={mod.path}
                to={mod.path}
                className={`flex items-center ${sidebarCollapsed ? 'justify-center px-2' : 'gap-3 px-4'} py-3 rounded-xl text-sm font-medium transition-all duration-200 ${
                  isActive
                    ? 'bg-gray-100 text-black shadow-sm'
                    : 'text-gray-900 hover:bg-gray-100 hover:text-black'
                }`}
                title={sidebarCollapsed ? mod.label : undefined}
              >
                {icon ?? null}
                {!sidebarCollapsed && <span>{mod.label}</span>}
              </Link>
            );
          })}
        </nav>

        <div className={`${sidebarCollapsed ? 'px-2' : 'px-4'} pb-6 space-y-2 border-t border-gray-200 pt-4 mt-auto`}>
          {bottomNavItems.map((item) => {
            const isActive = location.pathname === item.to;
            return (
              <Link
                key={item.to}
                to={item.to}
                className={`flex items-center ${sidebarCollapsed ? 'justify-center px-2' : 'gap-3 px-4'} py-3 rounded-xl text-sm font-medium transition-all duration-200 ${
                  isActive
                    ? 'bg-gray-100 text-black shadow-sm'
                    : 'text-gray-900 hover:bg-gray-100 hover:text-black'
                }`}
                title={sidebarCollapsed ? item.label : undefined}
              >
                {item.icon}
                {!sidebarCollapsed && <span>{item.label}</span>}
              </Link>
            );
          })}
          <button
            onClick={handleLogout}
            className={`w-full flex items-center ${sidebarCollapsed ? 'justify-center px-2' : 'gap-3 px-4'} py-3 rounded-xl text-sm font-medium transition-all duration-200 text-gray-900 hover:bg-gray-100 hover:text-black`}
            title={sidebarCollapsed ? 'Logout' : undefined}
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            {!sidebarCollapsed && <span>Logout</span>}
          </button>
        </div>
      </aside>

      <div className="flex-1 flex flex-col min-w-0 min-h-0 overflow-auto">
        {visibleNavItems.length <= 1 && !isSuperAdmin && (
          <div className="bg-amber-50 border-b border-amber-200 px-4 py-2 text-sm text-amber-800">
            No modules assigned. Contact your admin to assign modules in Configurator (role_module_permissions).
          </div>
        )}
        {content}
      </div>
    </div>
  );
}
